function getCurrentLang() {
    const userLang = navigator.language;
    // Mapeo de idiomas asiáticos que TMDB usa con formato específico
    const langMap = {
        'zh': 'zh-CN',
        'zh-CN': 'zh-CN',
        'zh-TW': 'zh-CN',
        'ja': 'ja',
        'ko': 'ko'
    };
    
    const shortLang = userLang.split('-')[0];
    
    // Si es idioma asiático, devolver el formato completo
    if (langMap[userLang]) return langMap[userLang];
    if (langMap[shortLang]) return langMap[shortLang];
    
    // Para el resto, usar el código corto (es, en, fr, de, it, pt)
    return translations[shortLang] ? shortLang : 'en';
}

function applyTranslations() {
    const lang = getCurrentLang();
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });
}

function t(key) {
    const lang = getCurrentLang();
    return translations[lang][key] || translations['en'][key] || key;
}

document.addEventListener('DOMContentLoaded', applyTranslations);